package baitap2024.crile;

public class crile {
    private double x = 1.0;
    private String color = "red";
    public crile() {

    }

    public crile(double x, String color) {
        this.x = x;
        this.color = color;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
